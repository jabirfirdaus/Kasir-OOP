    public void tambahtransaksi(){
    try {
        int jumlah = Integer.valueOf(txtjumlah.getText());
        int harga = Integer.valueOf(txtharga.getText());
        int total = jumlah * harga; // PERBAIKAN: Dikali, bukan ditambah
        
        txttotal.setText(String.valueOf(total));
        
        // Load data dulu ke tabel
        DefaultTableModel model = (DefaultTableModel)jTable1.getModel();
        model.addRow(new Object[]{
            txtnotrans.getText(),
            txtidbarang.getText(),
            txtnamabarang.getText(),
            txtjumlah.getText(),
            txtharga.getText(),
            String.valueOf(total) // Simpan total per baris
        });
        
        totalbiaya(); // Hitung ulang total belanja
        clear2();
        txtidbarang.requestFocus();
    } catch (NumberFormatException e) {
        JOptionPane.showMessageDialog(this, "Harap isi jumlah dan harga dengan angka yang benar!");
    }
}
