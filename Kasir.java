public void totalbiaya() {
    int jumlahbaris = jTable1.getRowCount();
    int totalbiaya = 0;
    for (int i = 0; i < jumlahbaris; i++) {
        // Ambil langsung dari kolom Total (Indeks 5) yang sudah dihitung di 'tambahtransaksi'
        int totalPerBaris = Integer.parseInt(jTable1.getValueAt(i, 5).toString());
        totalbiaya += totalPerBaris;
    }
    txttotal.setText(String.valueOf(totalbiaya));
    txttampil.setText("Rp "+ totalbiaya +",00");
}
    
